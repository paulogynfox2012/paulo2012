    private void AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarActionPerformed
        int indice = Tabela.getSelectedRow();
        DefaultTableModel dtmTabela = (DefaultTableModel)  Tabela.getModel ();
        Object [] dados = {Tp.getSelectedItem(),Num.getSelectedItem(),Cor.getSelectedItem(),Qnt.getText(),DtEnt.getText() ,Marca.getSelectedItem(),};
        dtmTabela.insertRow(indice, dados);
        dtmTabela.removeRow(indice+1);
    }//GEN-LAST:event_AtualizarActionPerformed

