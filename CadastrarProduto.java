    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
    
        if (Tabela.getSelectedRow() != -1){
            DefaultTableModel dtmTabela = (DefaultTableModel)  Tabela.getModel ();
            dtmTabela.removeRow(Tabela.getSelectedRow());
        }else{
            JOptionPane.showMessageDialog(null,"Selecione um Produto Para Excluir.");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_ExcluirActionPerformed

    private void TabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaMouseClicked
       if (Tabela.getSelectedRow() != -1){
           Tp.setSelectedItem(Tabela.getValueAt(Tabela.getSelectedRow (), 0).toString());
           Num.setSelectedItem(Tabela.getValueAt(Tabela.getSelectedRow (), 1).toString());
           Cor.setSelectedItem(Tabela.getValueAt(Tabela.getSelectedRow (), 2).toString());
           Qnt.setText(Tabela.getValueAt(Tabela.getSelectedRow (), 3).toString());
           DtEnt.setText(Tabela.getValueAt(Tabela.getSelectedRow (), 4).toString());
           Marca.setSelectedItem(Tabela.getValueAt(Tabela.getSelectedRow (), 5).toString());
                  
                  
                  
       }else{ JOptionPane.showMessageDialog(null,"Selecione um Produto Para Editar");
           
       }
        // TODO add your handling code here:
    }//GEN-LAST:event_TabelaMouseClicked

